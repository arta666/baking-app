package com.arman.baking.model;

public class Ingredient {
    private int quantity;
    private String measure;
    private String ingredient;


    public Ingredient() {
    }

    public int getQuantity() {
        return quantity;
    }

    public String getMeasure() {
        return measure;
    }

    public String getIngredient() {
        return ingredient;
    }
}
